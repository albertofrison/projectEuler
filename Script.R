# creating a function that returns if a number is palindrome
is_palindrome <- function (n) {
  original <- n
  remaninder <- 0
  reversed <- 0
  
  while (n != 0) {
    remainder <- n %% 10
    reversed <-  reversed * 10 + remainder
    n <- n %/% 10 
  }
  return (original == reversed)
}

